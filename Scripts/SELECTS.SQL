--TABELA PRINCIPAL
SELECT * FROM AVALIACAO

--TITULO DA AVALIAÇÃO
SELECT * FROM TITULO_AVALIACAO

--PERGUNTAS
SELECT * FROM AVALIACAO_PERGUNTAS


--MOVTO
SELECT * FROM MOVTO_AVALIACAO

--MOVTO AVALIAÇÃO FUNCIONARIO
SELECT MAF.FUNCIONARIO,   
  FROM MOVTO_AVALIACAO_FUNC MAF,
       MOVTO_AVALIACAO MA
 WHERE FUNCIONARIO = 203333
 
-- ITEM DA AVALIAÇÃO POR FUNCIONARIO
SELECT AFI.CHAVE CHAVE_AVALIACAO_FUNC_ITEM,
       MAF.FUNCIONARIO,
       MA.DESCRICAO,
       MA.PERIODO_INICIAL,
       MA.PERIODO_FINAL,
       TA.CODIGO CODIGO_TITULO_AVALIACAO,
       TA.DESCRICAO DESCRICAO_TITULO_AVALIACAO,
       AP.DESCRICAO_PERGUNTA,
       H.DESCRICAO_TITULO DESCRICAO_HABILIDADE,
       AP.RESPOSTA1,
       AP.RESPOSTA2,
       AP.RESPOSTA3,
       AP.RESPOSTA4,
       AP.RESPOSTA5,
       AP.RESPOSTA6,
       AP.RESPOSTA7,
       AP.RESPOSTA8,
       AP.RESPOSTA9,
       AP.RESPOSTA10,
       AFI.RESPOSTA
  FROM AVALIACAO_FUNC_ITEM AFI,
       MOVTO_AVALIACAO_FUNC MAF,
       MOVTO_AVALIACAO MA,
       AVALIACAO_PERGUNTAS AP,
       TITULO_AVALIACAO TA,
       CARGOXHABILIDADES CH,
       HABILIDADES H
 WHERE AFI.CHAVE_MOVTO_AVALIACAO_FUNC = MAF.CHAVE
   AND MAF.MOVTO_AVALIACAO = MA.CHAVE
   AND AFI.CHAVE_AVALIACAO_PERGUNTAS = AP.CHAVE
   AND AP.TITULO_AVALIACAO = TA.CHAVE
   AND AFI.CHAVE_CARGOXHABIL = CH.CHAVE(+)
   AND CH.HABILIDADE = H.CHAVE
   AND MA.AUTO_AVALIACAO = 'SIM'
   AND MAF.STATUS = 'ABERTO'
   
   AND MAF.FUNCIONARIO = 203319
   
 ORDER BY 
   